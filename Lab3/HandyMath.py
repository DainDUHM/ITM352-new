#Library of Handy reusable math functions

#Given two numbers, returns the midpoint
def midpoint(num1, num2):
    return ((num1 + num2)/2)

#Return the Suare root of the number 

def squareroot(number1):
    return number1 ** 0.5

#Return Exponent 
def exponent(number1, number2):
    return number1 ** number2

#Return Max number given 
def max(num1, num2):
    return num1 if num1 > num2 else num2

#Returns Min number given
def min(num1, num2):
    return num1 if num1 < num2 else num2



